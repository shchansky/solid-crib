// üí° –ü–†–ï–ò–ú–£–©–ï–°–¢–í–û: –ö–∞–∂–¥—ã–π –º–µ—Ç–æ–¥ –æ—Ç–≤–µ—á–∞–µ—Ç –∑–∞ –æ–¥–Ω—É –∫–æ–Ω–∫—Ä–µ—Ç–Ω—É—é –∑–∞–¥–∞—á—É
// üéØ –†–ï–ó–£–õ–¨–¢–ê–¢: –õ–µ–≥–∫–æ—Å—Ç—å –ø–æ–Ω–∏–º–∞–Ω–∏—è –∏ —Ç–µ—Å—Ç–∏—Ä–æ–≤–∞–Ω–∏—è
class ShapeProcessor {
    radius?: number;
    width?: number;
    height?: number;

    constructor(radius?: number, width?: number, height?: number) {
        this.radius = radius;
        this.width = width;
        this.height = height;
    }

    // ‚úÖ SRP: –¢–æ–ª—å–∫–æ –≤–∞–ª–∏–¥–∞—Ü–∏—è –∫—Ä—É–≥–∞
    // üí° –ü–†–ï–ò–ú–£–©–ï–°–¢–í–û: –û–¥–Ω–∞ –æ—Ç–≤–µ—Ç—Å—Ç–≤–µ–Ω–Ω–æ—Å—Ç—å - –ø—Ä–æ–≤–µ—Ä–∫–∞ –≤–∞–ª–∏–¥–Ω–æ—Å—Ç–∏
    // üéØ –†–ï–ó–£–õ–¨–¢–ê–¢: –õ–µ–≥–∫–æ —Ç–µ—Å—Ç–∏—Ä–æ–≤–∞—Ç—å –∏ –∏–∑–º–µ–Ω—è—Ç—å
    private isValidCircle(): boolean {
        return !!this.radius && this.radius > 0;
    }

    // ‚úÖ SRP: –¢–æ–ª—å–∫–æ –≤–∞–ª–∏–¥–∞—Ü–∏—è –ø—Ä—è–º–æ—É–≥–æ–ª—å–Ω–∏–∫–∞
    // üí° –ü–†–ï–ò–ú–£–©–ï–°–¢–í–û: –û–¥–Ω–∞ –æ—Ç–≤–µ—Ç—Å—Ç–≤–µ–Ω–Ω–æ—Å—Ç—å - –ø—Ä–æ–≤–µ—Ä–∫–∞ –≤–∞–ª–∏–¥–Ω–æ—Å—Ç–∏
    // üéØ –†–ï–ó–£–õ–¨–¢–ê–¢: –õ–µ–≥–∫–æ —Ç–µ—Å—Ç–∏—Ä–æ–≤–∞—Ç—å –∏ –∏–∑–º–µ–Ω—è—Ç—å
    private isValidRectangle(): boolean {
        return !!this.width  && !!this.height && 
               this.width > 0 && this.height > 0;
    }

    // ‚úÖ SRP: –¢–æ–ª—å–∫–æ —Ä–∞—Å—á–µ—Ç –ø–ª–æ—â–∞–¥–∏ –∫—Ä—É–≥–∞
    // üí° –ü–†–ï–ò–ú–£–©–ï–°–¢–í–û: –û–¥–Ω–∞ –æ—Ç–≤–µ—Ç—Å—Ç–≤–µ–Ω–Ω–æ—Å—Ç—å - –º–∞—Ç–µ–º–∞—Ç–∏—á–µ—Å–∫–∏–π —Ä–∞—Å—á–µ—Ç
    // üéØ –†–ï–ó–£–õ–¨–¢–ê–¢: –õ–µ–≥–∫–æ —Ç–µ—Å—Ç–∏—Ä–æ–≤–∞—Ç—å –∏ –∏–∑–º–µ–Ω—è—Ç—å —Ñ–æ—Ä–º—É–ª—É
    private calculateCircleArea(): number {

        if(!this.radius) {
            throw new Error('Invalid circle radius');
        }

        return Math.PI * this.radius * this.radius;
    }

    // ‚úÖ SRP: –¢–æ–ª—å–∫–æ —Ä–∞—Å—á–µ—Ç –ø–µ—Ä–∏–º–µ—Ç—Ä–∞ –∫—Ä—É–≥–∞
    // üí° –ü–†–ï–ò–ú–£–©–ï–°–¢–í–û: –û–¥–Ω–∞ –æ—Ç–≤–µ—Ç—Å—Ç–≤–µ–Ω–Ω–æ—Å—Ç—å - –º–∞—Ç–µ–º–∞—Ç–∏—á–µ—Å–∫–∏–π —Ä–∞—Å—á–µ—Ç
    // üéØ –†–ï–ó–£–õ–¨–¢–ê–¢: –õ–µ–≥–∫–æ —Ç–µ—Å—Ç–∏—Ä–æ–≤–∞—Ç—å –∏ –∏–∑–º–µ–Ω—è—Ç—å —Ñ–æ—Ä–º—É–ª—É
    private calculateCirclePerimeter(): number {

        if(!this.radius) {
            throw new Error('Invalid circle radius');
        }

        return 2 * Math.PI * this.radius;
    }

    // ‚úÖ SRP: –¢–æ–ª—å–∫–æ —Ä–∞—Å—á–µ—Ç –ø–ª–æ—â–∞–¥–∏ –ø—Ä—è–º–æ—É–≥–æ–ª—å–Ω–∏–∫–∞
    // üí° –ü–†–ï–ò–ú–£–©–ï–°–¢–í–û: –û–¥–Ω–∞ –æ—Ç–≤–µ—Ç—Å—Ç–≤–µ–Ω–Ω–æ—Å—Ç—å - –º–∞—Ç–µ–º–∞—Ç–∏—á–µ—Å–∫–∏–π —Ä–∞—Å—á–µ—Ç
    // üéØ –†–ï–ó–£–õ–¨–¢–ê–¢: –õ–µ–≥–∫–æ —Ç–µ—Å—Ç–∏—Ä–æ–≤–∞—Ç—å –∏ –∏–∑–º–µ–Ω—è—Ç—å —Ñ–æ—Ä–º—É–ª—É
    private calculateRectangleArea(): number {
        return (this.width ?? 0) * (this.height ?? 0);
    }

    // ‚úÖ SRP: –¢–æ–ª—å–∫–æ —Ä–∞—Å—á–µ—Ç –ø–µ—Ä–∏–º–µ—Ç—Ä–∞ –ø—Ä—è–º–æ—É–≥–æ–ª—å–Ω–∏–∫–∞
    // üí° –ü–†–ï–ò–ú–£–©–ï–°–¢–í–û: –û–¥–Ω–∞ –æ—Ç–≤–µ—Ç—Å—Ç–≤–µ–Ω–Ω–æ—Å—Ç—å - –º–∞—Ç–µ–º–∞—Ç–∏—á–µ—Å–∫–∏–π —Ä–∞—Å—á–µ—Ç
    // üéØ –†–ï–ó–£–õ–¨–¢–ê–¢: –õ–µ–≥–∫–æ —Ç–µ—Å—Ç–∏—Ä–æ–≤–∞—Ç—å –∏ –∏–∑–º–µ–Ω—è—Ç—å —Ñ–æ—Ä–º—É–ª—É
    private calculateRectanglePerimeter(): number {
        return 2 * ((this.width ?? 0) + (this.height ?? 0));
    }

    // ‚úÖ SRP: –ö–æ–æ—Ä–¥–∏–Ω–∞—Ç–æ—Ä –¥–ª—è –∫—Ä—É–≥–∞ - —Ç–æ–ª—å–∫–æ –∫–æ–æ—Ä–¥–∏–Ω–∞—Ü–∏—è
    // üí° –ü–†–ï–ò–ú–£–©–ï–°–¢–í–û: –û–¥–Ω–∞ –æ—Ç–≤–µ—Ç—Å—Ç–≤–µ–Ω–Ω–æ—Å—Ç—å - –∫–æ–æ—Ä–¥–∏–Ω–∞—Ü–∏—è –≤–∞–ª–∏–¥–∞—Ü–∏–∏ –∏ —Ä–∞—Å—á–µ—Ç–æ–≤
    // üéØ –†–ï–ó–£–õ–¨–¢–ê–¢: –ß–µ—Ç–∫–∞—è —Å—Ç—Ä—É–∫—Ç—É—Ä–∞ –∏ –ª–µ–≥–∫–æ–µ –ø–æ–Ω–∏–º–∞–Ω–∏–µ
    public processCircle(): {area: number, perimeter: number} {
        if (!this.isValidCircle()) {
            throw new Error('Invalid circle radius');
        }

        return {area: this.calculateCircleArea() , perimeter: this.calculateCirclePerimeter()};        
    }

    // ‚úÖ SRP: –ö–æ–æ—Ä–¥–∏–Ω–∞—Ç–æ—Ä –¥–ª—è –ø—Ä—è–º–æ—É–≥–æ–ª—å–Ω–∏–∫–∞ - —Ç–æ–ª—å–∫–æ –∫–æ–æ—Ä–¥–∏–Ω–∞—Ü–∏—è
    // üí° –ü–†–ï–ò–ú–£–©–ï–°–¢–í–û: –û–¥–Ω–∞ –æ—Ç–≤–µ—Ç—Å—Ç–≤–µ–Ω–Ω–æ—Å—Ç—å - –∫–æ–æ—Ä–¥–∏–Ω–∞—Ü–∏—è –≤–∞–ª–∏–¥–∞—Ü–∏–∏ –∏ —Ä–∞—Å—á–µ—Ç–æ–≤
    // üéØ –†–ï–ó–£–õ–¨–¢–ê–¢: –ß–µ—Ç–∫–∞—è —Å—Ç—Ä—É–∫—Ç—É—Ä–∞ –∏ –ª–µ–≥–∫–æ–µ –ø–æ–Ω–∏–º–∞–Ω–∏–µ
    public processRectangle(): {area: number, perimeter: number} {
        if (!this.isValidRectangle()) {
            throw new Error('Invalid rectangle dimensions');
        }

        return {area: this.calculateRectangleArea(), perimeter: this.calculateRectanglePerimeter()};        
    }
}

// ‚úÖ –î–ï–ú–û–ù–°–¢–†–ê–¶–ò–Ø –ü–†–ï–ò–ú–£–©–ï–°–¢–í: –ß–µ—Ç–∫–æ–µ —Ä–∞–∑–¥–µ–ª–µ–Ω–∏–µ –æ—Ç–≤–µ—Ç—Å—Ç–≤–µ–Ω–Ω–æ—Å—Ç–µ–π
// üí° –ü–†–ï–ò–ú–£–©–ï–°–¢–í–û: –ö–∞–∂–¥—ã–π –º–µ—Ç–æ–¥ –¥–µ–ª–∞–µ—Ç —Ç–æ–ª—å–∫–æ –æ–¥–Ω—É –≤–µ—â—å
// üéØ –†–ï–ó–£–õ–¨–¢–ê–¢: –õ–µ–≥–∫–æ—Å—Ç—å –∏—Å–ø–æ–ª—å–∑–æ–≤–∞–Ω–∏—è –∏ –ø–æ–Ω–∏–º–∞–Ω–∏—è
