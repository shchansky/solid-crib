function getCircleArea(radius: number): number {
    return Math.PI * radius * radius;
}

// ‚úÖ SRP: –¢–æ–ª—å–∫–æ —Ä–∞—Å—á–µ—Ç –ø–µ—Ä–∏–º–µ—Ç—Ä–∞ –∫—Ä—É–≥–∞
