    // üí° –ü–†–û–ë–õ–ï–ú–ê: –î—Ä—É–≥–æ–π —Ñ–æ—Ä–º–∞—Ç –∏–Ω—Ñ–æ—Ä–º–∞—Ü–∏–∏ –Ω–∞—Ä—É—à–∞–µ—Ç –æ–∂–∏–¥–∞–Ω–∏—è
    // üéØ –†–ï–ó–£–õ–¨–¢–ê–¢: –ù–µ–æ–∂–∏–¥–∞–Ω–Ω–æ–µ –ø–æ–≤–µ–¥–µ–Ω–∏–µ –¥–ª—è –∫–ª–∏–µ–Ω—Ç–æ–≤
